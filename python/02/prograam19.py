# 1. One string using single quotes
single_quote_string = 'This is a string using single quotes.'
print(f"Single Quotes: {single_quote_string}")

# 2. One string using double quotes
double_quote_string = "This is a string using double quotes."
print(f"Double Quotes: {double_quote_string}")

# 3. One multi-line string using triple quotes
triple_quote_string = """This is a multi-line string.
It can span multiple lines
without using the \\n character."""
print(f"Triple Quotes:\n{triple_quote_string}")