# Integer (int) - whole number
my_int = 10 

# Float (float) - decimal number
my_float = 20.5

# Complex (complex) - number with real and imaginary parts
my_complex = 1 + 2j

# String (str) - sequence of characters
my_string = "Python Basics"

# List (list) - ordered, mutable sequence
my_list = [10, "A", 3.14]

# Tuple (tuple) - ordered, immutable sequence
my_tuple = (1, 2, "B")

# Dictionary (dict) - unordered collection of key-value pairs
my_dict = {"a": 1, "b": 2}

# Set (set) - unordered collection of unique elements
my_set = {5, 10, 15}

# Boolean (bool) - True or False
my_bool = True

# None (NoneType) - represents the absence of a value
my_none = None

# Print the assigned values and their types to verify
print(f"Integer: {my_int}, Type: {type(my_int)}")
print(f"Float: {my_float}, Type: {type(my_float)}")
print(f"Complex: {my_complex}, Type: {type(my_complex)}")
print(f"String: {my_string}, Type: {type(my_string)}")
print(f"List: {my_list}, Type: {type(my_list)}")
print(f"Tuple: {my_tuple}, Type: {type(my_tuple)}")
print(f"Dictionary: {my_dict}, Type: {type(my_dict)}")
print(f"Set: {my_set}, Type: {type(my_set)}")
print(f"Boolean: {my_bool}, Type: {type(my_bool)}")
print(f"None: {my_none}, Type: {type(my_none)}")